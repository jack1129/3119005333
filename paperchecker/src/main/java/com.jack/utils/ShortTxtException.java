package com.jack.utils;

public class ShortTxtException extends Exception {
    public ShortTxtException() {
        super();
    }

    public ShortTxtException(String message) {
        super(message);
    }

    public ShortTxtException(String message, Throwable cause) {
        super(message, cause);
    }

    public ShortTxtException(Throwable cause) {
        super(cause);
    }

}
